class AppointmentsController < ApplicationController
  before_action :authenticate_user!
  def index
    if current_user.is_provider
      @appointments = Appointment.for_provider(current_user.provider.id)
    else
      @appointments = Appointment.for_patient(current_user.patient.id)
    end
  end
  # GET /appointments/1
  # GET /appointments/1.json
  def show
  end

  def book
    current_user.patient.assign_provider(params[:provider_id])
    @patient = current_user.patient
    @patients_dr = @patient.provider
    @availabilities = @patients_dr.availabilities
  end

  # GET /appointments/new
  def new
    @availability = Availability.find_by_id params[:availability_id]
    @appointment = Appointment.create
    @appointment.book_appointment(@availability, current_user.patient)

    redirect_to :back
  end

  # GET /appointments/1/edit
  def edit
  end

  # POST /appointments
  # POST /appointments.json
  def create
    # binding.pry
    # @appointment = Appointment.new(appointment_params)
    respond_to do |format|
      if @appointment.save
        format.html { redirect_to @appointment, notice: 'Appointment was successfully created.' }
        format.json { render :show, status: :created, location: @appointment }
      else
        format.html { render :new }
        format.json { render json: @appointment.errors, status: :unprocessable_entity }
      end
    end
  end

  # PATCH/PUT /appointments/1
  # PATCH/PUT /appointments/1.json
  def update
    respond_to do |format|
      if @appointment.update(appointment_params)
        format.html { redirect_to @appointment, notice: 'Appointment was successfully updated.' }
        format.json { render :show, status: :ok, location: @appointment }
      else
        format.html { render :edit }
        format.json { render json: @appointment.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /appointments/1
  # DELETE /appointments/1.json
  def destroy
    @appointment = Appointment.find(params[:id])
    @appointment.destroy
    respond_to do |format|
      format.html { redirect_to :back, notice: 'Appointment was successfully destroyed.' }
      format.json { head :no_content }
    end
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    # def set_appointment
    #  @appointment = Appointment.find(params[:id])
    # end

    # Never trust parameters from the scary internet, only allow the white list through.
    def appointment_params
      params.require(:appointment).permit(:provider_id, :patient_id, :start, :end)
    end
end

